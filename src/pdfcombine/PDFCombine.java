    private class CombinePDF extends Thread {

        public void run() {

            if (l1.getSize() != 0) {

                String result = lOutputPath.getText().substring(23, lOutputPath.getText().length() - 18);
                if (!result.endsWith(".pdf")) {
                    result = result + ".pdf";

                }
                try {
                    File file = new File(result);

                    File parent = new File(file.getParent());
                    if (parent.exists()) {
                        String[] files = new String[l1.getSize()];
                        for (int i = 0; i < l1.getSize(); i++) {
                            files[i] = l1.getElementAt(i);
                        }
                        mergeFiles(files, result, true);
                    } else {
                        JOptionPane.showMessageDialog(null, "Please change Output Path");
                    }
                    l1.clear();

                } catch (IOException ex) {
                    Logger.getLogger(PDFCombine.class.getName()).log(Level.SEVERE, null, ex);
                    JOptionPane.showMessageDialog(null, ex);
                } catch (DocumentException ex) {
                    Logger.getLogger(PDFCombine.class.getName()).log(Level.SEVERE, null, ex);
                    JOptionPane.showMessageDialog(null, ex);
                }
            }
        }

        public void mergeFiles(String[] files, String result, boolean smart) throws IOException, DocumentException {
            Document document = new Document();
            PdfCopy copy;

            if (smart) {
                copy = new PdfSmartCopy(document, new FileOutputStream(result));
            } else {
                copy = new PdfCopy(document, new FileOutputStream(result));
            }
            document.open();
            PdfReader[] reader = new PdfReader[files.length];
            float progress = 0.00f;
            float perFileProgress = 100.00f / files.length;
            System.out.print(files.length);
            for (int i = 0; i < files.length; i++) {
                reader[i] = new PdfReader(files[i]);
                copy.addDocument(reader[i]);
                copy.freeReader(reader[i]);
                reader[i].close();
                progress = progress + perFileProgress;
                progressBar.setValue((int) Math.ceil(progress));
                progressBar.setStringPainted(true);

            }
            document.close();
        }
    }
