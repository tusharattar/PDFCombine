    private void bDown1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bDown1ActionPerformed
        if (l1.getSize() != 0) {
            int size = l1.getSize();
            String[] list = new String[size];
            for (int i = 0; i < l1.getSize(); i++) {
                list[i] = l1.getElementAt(i);
            }
            l1.removeAllElements();
            for (int i = size - 1; i > -1; i--) {
                l1.addElement(list[i]);
            }
        }
    }//GEN-LAST:event_bDown1ActionPerformed

