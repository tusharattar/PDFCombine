    void addFilePathtoList(File[] file, String source) {
        int count = 0;
        for (File file1 : file) {
            if (file1.isFile() && file1.toString().endsWith(".pdf")) {
                l1.addElement(file1 + "");
                count++;
            }
        }
        if (count == 0) {
            if (source.equalsIgnoreCase("folder")) {
                JOptionPane.showMessageDialog(null, "Selected folder diesn't contain any PDF files", "No PDF File Selected", WIDTH);
            }

            if (source.equalsIgnoreCase("file")) {
                JOptionPane.showMessageDialog(null, "No PDF file selected", "No PDF File Selected", WIDTH);
            }
        }
        itemList.setModel(l1);
    }

